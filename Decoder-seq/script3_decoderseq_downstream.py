def run_downstream_analysis(
    adata, n_top_genes=3000, n_pcs=30, n_neighbors=15, resolution=0.5,
    cluster_key='leiden', use_in_tissue=True, copy=True
):
    """
    Run standard Scanpy downstream analysis:
    QC filtering -> normalization -> HVG selection -> PCA -> neighbors -> UMAP -> Leiden clustering.

    Parameters
    ----------
    adata : AnnData
        AnnData object generated by build_adata_75x75.
    n_top_genes : int
        Number of highly variable genes.
    n_pcs : int
        Number of principal components used for neighbor graph.
    n_neighbors : int
        Number of neighbors for neighborhood graph.
    resolution : float
        Leiden clustering resolution.
    cluster_key : str
        Column name in adata.obs to store cluster labels.
    use_in_tissue : bool
        If True, only keep spots with in_tissue == '1' for downstream analysis.
    copy : bool
        If True, return a new AnnData object.

    Returns
    -------
    adata : AnnData
        Processed AnnData with PCA, UMAP and Leiden clustering.
    """

    if copy:
        adata = adata.copy()

    # 1) Optional: only keep tissue spots
    if use_in_tissue and 'in_tissue' in adata.obs.columns:
        adata = adata[adata.obs['in_tissue'].astype(str) == '1'].copy()

    # 2) Basic filtering
    sc.pp.filter_cells(adata, min_genes=200)
    sc.pp.filter_genes(adata, min_cells=3)

    # 3) Save raw counts before normalization
    adata.layers['counts'] = adata.X.copy()

    # 4) Normalize and log transform
    sc.pp.normalize_total(adata, target_sum=1e4)
    sc.pp.log1p(adata)

    # 5) Store normalized log data in raw for marker visualization
    adata.raw = adata

    # 6) Highly variable genes
    sc.pp.highly_variable_genes(
        adata, n_top_genes=n_top_genes, flavor='seurat'
    )

    # 7) PCA using HVGs
    sc.pp.scale(adata, max_value=10, zero_center=False)
    sc.tl.pca(
        adata, n_comps=n_pcs, mask_var='highly_variable', svd_solver='arpack'
    )

    # 8) Neighbor graph, UMAP, Leiden clustering
    sc.pp.neighbors(
        adata, n_neighbors=n_neighbors, n_pcs=n_pcs
    )

    sc.tl.umap(adata)

    sc.tl.leiden(
        adata, resolution=resolution, key_added=cluster_key
    )

    return adata

def find_marker_genes(
    adata, cluster_key='leiden', method='wilcoxon',
    pts=True, n_genes=100, save_path=None
):
    """
    Find marker genes for each cluster using scanpy.tl.rank_genes_groups.

    Parameters
    ----------
    adata : AnnData
        Processed AnnData after clustering.
    cluster_key : str
        Cluster column in adata.obs.
    method : str
        Method for marker detection: 'wilcoxon', 't-test', 'logreg'.
    pts : bool
        Whether to calculate fraction of cells/spots expressing genes.
    n_genes : int
        Number of genes to export per cluster.
    save_path : str or None
        If provided, save marker table as csv.

    Returns
    -------
    marker_df : pandas.DataFrame
        Marker gene table.
    """

    if cluster_key not in adata.obs.columns:
        raise ValueError(f"{cluster_key} is not found in adata.obs")

    sc.tl.rank_genes_groups(
        adata,
        groupby=cluster_key,
        method=method,
        pts=pts
    )

    marker_df = sc.get.rank_genes_groups_df(
        adata,
        group=None
    )

    if n_genes is not None:
        marker_df = (
            marker_df
            .groupby('group', group_keys=False)
            .head(n_genes)
        )

    if save_path is not None:
        marker_df.to_csv(save_path, index=False)
        print(f"Saved marker genes to: {save_path}")

    return marker_df

def plot_clusters(
    adata, cluster_key='leiden', sample_name='sample1',
    out_dir='.', point_size=20, spatial_size=1.2, dpi=300
):
    """
    Plot UMAP clustering, spatial clustering, and spatial clustering on H&E.

    Parameters
    ----------
    adata : AnnData
        Processed AnnData after clustering.
    cluster_key : str
        Cluster column in adata.obs.
    sample_name : str
        Library/sample name used in adata.uns['spatial'].
    out_dir : str
        Output directory.
    point_size : float
        Point size for UMAP/spatial embedding.
    spatial_size : float
        Spot size for sc.pl.spatial.
    dpi : int
        Figure DPI.
    """
    
    os.makedirs(out_dir, exist_ok=True)

    # UMAP cluster plot
    sc.pl.umap(
        adata, color=cluster_key, frameon=False, show=False
    )
    plt.savefig(
        os.path.join(out_dir, f'{sample_name}_umap_{cluster_key}.png'),
        dpi=dpi, bbox_inches='tight'
    )
    plt.close()

    # Spatial cluster plot without H&E background
    sc.pl.embedding(
        adata, basis='spatial', color=cluster_key, size=point_size,
        frameon=True, show=False
    )
    plt.gca().set_aspect('equal')
    plt.gca().invert_yaxis()
    plt.savefig(
        os.path.join(out_dir, f'{sample_name}_spatial_{cluster_key}.png'),
        dpi=dpi,
        bbox_inches='tight'
    )
    plt.close()

    # Spatial cluster plot on H&E
    if 'spatial' in adata.uns and sample_name in adata.uns['spatial']:
        sc.pl.spatial(
            adata,
            color=cluster_key,
            library_id=sample_name,
            img_key='hires',
            size=spatial_size,
            frameon=False,
            show=False
        )
        plt.savefig(
            os.path.join(out_dir, f'{sample_name}_HE_{cluster_key}.png'),
            dpi=dpi,
            bbox_inches='tight'
        )
        plt.close()

def plot_marker_genes(
    adata,
    marker_df,
    cluster_key='leiden',
    sample_name='sample1',
    out_dir='.',
    top_n=5,
    dpi=300
):
    """
    Plot top marker genes using dotplot and spatial plots.

    Parameters
    ----------
    adata : AnnData
        Processed AnnData.
    marker_df : pandas.DataFrame
        Marker gene table returned by find_marker_genes.
    cluster_key : str
        Cluster column in adata.obs.
    sample_name : str
        Library/sample name.
    out_dir : str
        Output directory.
    top_n : int
        Number of top marker genes per cluster to visualize.
    dpi : int
        Figure DPI.
    """
    out_dir = os.path.join(out_dir, 'Downstreamfigures')
    os.makedirs(out_dir, exist_ok=True)

    # Select top marker genes from each cluster
    top_markers = (
        marker_df
        .groupby('group', group_keys=False)
        .head(top_n)['names']
        .dropna()
        .unique()
        .tolist()
    )

    if len(top_markers) == 0:
        print("No marker genes found for plotting.")
        return

    # Dotplot
    sc.pl.dotplot(
        adata,
        var_names=top_markers,
        groupby=cluster_key,
        standard_scale='var',
        show=False
    )
    plt.savefig(
        os.path.join(out_dir, f'{sample_name}_top_marker_dotplot.png'),
        dpi=dpi,
        bbox_inches='tight'
    )
    plt.close()

    # Spatial plot for top marker genes
    for gene in top_markers:
        if gene not in adata.var_names:
            continue

        sc.pl.embedding(
            adata,
            basis='spatial',
            color=gene,
            cmap='Spectral_r',
            size=20,
            frameon=True,
            show=False
        )
        plt.gca().set_aspect('equal')
        plt.gca().invert_yaxis()
        plt.savefig(
            os.path.join(out_dir, f'{sample_name}_spatial_marker_{gene}.png'),
            dpi=dpi,
            bbox_inches='tight'
        )
        plt.close()

        if 'spatial' in adata.uns and sample_name in adata.uns['spatial']:
            sc.pl.spatial(
                adata,
                color=gene,
                library_id=sample_name,
                img_key='hires',
                cmap='Spectral_r',
                size=1.2,
                frameon=False,
                show=False
            )
            plt.savefig(
                os.path.join(out_dir, f'{sample_name}_HE_marker_{gene}.png'),
                dpi=dpi,
                bbox_inches='tight'
            )
            plt.close()

def run_full_scanpy_pipeline(
    adata,
    sample_name='sample1',
    out_dir='.',
    n_top_genes=3000,
    n_pcs=30,
    n_neighbors=15,
    resolution=0.5,
    cluster_key='leiden',
    use_in_tissue=True,
    marker_method='wilcoxon',
    top_marker_genes=100,
    plot_top_n_marker=5,
    save_h5ad=True
):
    """
    Full downstream Scanpy pipeline:
    preprocessing, dimensionality reduction, clustering, marker detection and plotting.

    Returns
    -------
    adata_processed : AnnData
        Processed AnnData.
    marker_df : pandas.DataFrame
        Marker gene table.
    """

    os.makedirs(out_dir, exist_ok=True)

    print("Running downstream analysis...")
    adata_processed = run_downstream_analysis(
        adata,
        n_top_genes=n_top_genes,
        n_pcs=n_pcs,
        n_neighbors=n_neighbors,
        resolution=resolution,
        cluster_key=cluster_key,
        use_in_tissue=use_in_tissue,
        copy=True
    )

    print("Plotting clusters...")
    plot_clusters(
        adata_processed,
        cluster_key=cluster_key,
        sample_name=sample_name,
        out_dir=out_dir
    )

    print("Finding marker genes...")
    marker_df = find_marker_genes(
        adata_processed,
        cluster_key=cluster_key,
        method=marker_method,
        n_genes=top_marker_genes,
        save_path=os.path.join(out_dir, f'{sample_name}_{cluster_key}_marker_genes.csv')
    )

    print("Plotting marker genes...")
    plot_marker_genes(
        adata_processed,
        marker_df,
        cluster_key=cluster_key,
        sample_name=sample_name,
        out_dir=out_dir,
        top_n=plot_top_n_marker
    )

    if save_h5ad:
        h5ad_path = os.path.join(out_dir, f'{sample_name}_processed.h5ad')
        adata_processed.write_h5ad(h5ad_path)
        print(f"Saved processed AnnData to: {h5ad_path}")

    print("Done.")
    return adata_processed, marker_df


