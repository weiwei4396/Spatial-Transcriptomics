"""
75x75 chip pipeline: STARsolo + H&E (with on-image barcode markers) -> AnnData -> QC plot.

Differences from st_processing_rew.get_adata_STARsolo:
- No separate brightfield/spot-marker image
- No HE/Spot affine alignment (HE_point/Spot_point are gone)
- Three corner points are picked directly on the H&E image
- Grid is generated by linear interpolation between those corners (same idea
  as get_piexl_coods, but simpler because we don't need 9 region branches)
"""

import os
import time
import numpy as np
import pandas as pd
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt 
import scanpy as sc
import scipy.sparse as sp
from scipy.io import mmread
from anndata import AnnData
import cv2

# ----------------------------------------------------------------------
# Image utilities
# ----------------------------------------------------------------------
def load_image(filename):
    """Load image of any size (handles big histology slides)."""
    from PIL import Image, ImageFile
    ImageFile.LOAD_TRUNCATED_IMAGES = True
    Image.MAX_IMAGE_PIXELS = None
    img = np.array(Image.open(filename))
    if img.ndim == 3 and img.shape[-1] == 4:
        img = img[..., :3]
    elif img.ndim == 2:
        img = cv2.cvtColor(img, cv2.COLOR_GRAY2RGB)
    return img

# ----------------------------------------------------------------------
# Grid generation from three corner points
# ----------------------------------------------------------------------
def get_pixel_grid(corner_X1Y1, corner_Xmax_Y1, corner_X1_Ymax, n_x=75, n_y=75):
    """
    Compute pixel positions for every spot on a regular n_x by n_y grid
    using three corner anchor points on the H&E image.
    根据三个顶点位置推算所有的barcode的位置, 这个芯片有75个barcode在X方向, 75个barcode在Y方向, 
    共5625个barcode. 通过线性插值的方式推算出每个barcode在HE图上的位置.
    
    Parameters
    ----------
    corner_X1Y1 : tuple (col, row)        # X=1, Y=1   (top-left in barcode space)
    corner_Xmax_Y1 : tuple (col, row)     # X=n_x, Y=1 (top-right)
    corner_X1_Ymax : tuple (col, row)     # X=1, Y=n_y (bottom-left)

    Returns
    -------
    pixel_coords : (n_x*n_y, 2) ndarray of int   # (col, row) on the full-res image; 2列的像素坐标;
    grid_coords  : (n_x*n_y, 2) ndarray of int   # (X, Y) barcode coords (1-indexed); 每个barcode的索引位置, 2列分别从1到75;
    spot_pitch   : float                         # mean spot-to-spot pixel distance
    """
    p11 = np.asarray(corner_X1Y1, dtype=float)
    pX1 = np.asarray(corner_Xmax_Y1, dtype=float)
    p1Y = np.asarray(corner_X1_Ymax, dtype=float)

    vec_x = (pX1 - p11) / (n_x - 1)   # one step in +X (column-direction on chip)
    vec_y = (p1Y - p11) / (n_y - 1)   # one step in +Y (row-direction on chip)

    pixel_coords = np.empty((n_x * n_y, 2), dtype=int)
    grid_coords  = np.empty((n_x * n_y, 2), dtype=int)
    k = 0
    for y in range(1, n_y + 1):
        for x in range(1, n_x + 1):
            pt = p11 + (x - 1) * vec_x + (y - 1) * vec_y
            pixel_coords[k] = np.round(pt).astype(int)
            grid_coords[k]  = (x, y)
            k += 1
    
    # np.linalg.norm(vec_x) 表示 sqrt(vec_x[0]^2+vec_x[1]^2) 计算每个barcode在X方向上的像素距离;
    # np.linalg.norm(vec_y) 计算每个barcode在Y方向上的像素距离; 
    # 两者取平均作为最终的spot_pitch, 两个spot之间的平均距离;
    spot_pitch = (np.linalg.norm(vec_x) + np.linalg.norm(vec_y)) / 2
    return pixel_coords, grid_coords, spot_pitch


def visualize_grid(he_image_path, pixel_coords, every=2, save_path=None):
    """
    Overlay the inferred grid on the H&E image for a quick sanity check.
    将所有的barcode网格点都在H&E染色图上展示;

    参数：
    he_image_path: H&E染色图的路径;
    pixel_coords: 每个barcode在HE图上的像素坐标;
    every: 每隔多少个点展示一个点, 默认是2, 也就是展示一半的点;
    """
    # 读取H&E染色图 -> (height, width, 3)
    img = load_image(he_image_path)
    # 创建一个matplotlib画布和坐标轴;
    fig, ax = plt.subplots(figsize=(12, 12))
    # 把H&E图像显示出来, 作为背景图;
    ax.imshow(img)
    # 每隔every个点画一个红色的散点, 代表barcode的位置; s为点大小;
    # 点透明度为0.6, edgecolors='none'表示点没有边框;
    ax.scatter(pixel_coords[::every, 0], pixel_coords[::every, 1],
               s=3, c='red', alpha=0.6, edgecolors='none')
    ax.set_title('Inferred 75x75 grid on H&E')
    # 自动调整图像布局;
    plt.tight_layout()
    if save_path:
        plt.savefig(save_path, dpi=150, bbox_inches='tight')


