# Tissue mask (Otsu, like the original get_HE_mask)
# ----------------------------------------------------------------------
def rgb2gray(rgb):
    return np.dot(rgb[..., :3], [0.2989, 0.5870, 0.1140]).astype('uint8')


def get_HE_mask(img_HE):
    from skimage import filters
    image = rgb2gray(img_HE)
    thresh = filters.threshold_otsu(image)
    _, image_b = cv2.threshold(image, thresh, 250, cv2.THRESH_BINARY_INV)
    mask = np.zeros(image.shape, dtype=np.float32)
    contours, _ = cv2.findContours(image_b, cv2.RETR_EXTERNAL,
                                   cv2.CHAIN_APPROX_NONE)
    if not contours:
        return mask
    areas = [cv2.contourArea(c) for c in contours]
    mask = cv2.drawContours(mask, contours, int(np.argmax(areas)),
                            1, cv2.FILLED)
    return mask

def transform_grid_coords(coords_raw, n_x=75, n_y=75, mode='identity'):
    x = coords_raw[:, 0].astype(int)
    y = coords_raw[:, 1].astype(int)

    if mode == 'identity':
        x2, y2 = x, y

    elif mode == 'swap_xy':
        x2, y2 = y, x

    elif mode == 'rotate_ccw_90':
        x2, y2 = y, n_x + 1 - x

    elif mode == 'rotate_cw_90':
        x2, y2 = n_y + 1 - y, x

    elif mode == 'rotate_180':
        x2, y2 = n_x + 1 - x, n_y + 1 - y

    elif mode == 'flip_x':
        x2, y2 = n_x + 1 - x, y

    elif mode == 'flip_y':
        x2, y2 = x, n_y + 1 - y

    else:
        raise ValueError(f"Unknown transform mode: {mode}")

    return np.column_stack([x2, y2]).astype(int)


# ----------------------------------------------------------------------
# Main builder 从STARsolo和H&E染色图构建一个Visium格式的AnnData对象;
# ----------------------------------------------------------------------
def build_adata_75x75(starsolo_path, barcode_position_file, he_image_path,
                      corner_X1Y1, corner_Xmax_Y1, corner_X1_Ymax,
                      sample_name='sample1', n_x=75, n_y=75,
                      use_em=False, use_filtered=True,
                      species='mouse',
                      compute_mask=True, res_um=None,
                      coord_transform='identity'):
    """
    Build a Visium-style AnnData from STARsolo + H&E (markers on H&E).
    从STARsolo和H&E染色图构建一个Visium格式的AnnData对象;

    Parameters
    ----------
    starsolo_path : str
        Folder containing Gene/raw/ (and Gene/filtered/).
    barcode_position_file : str
        Tab-separated file with at least 3 columns: barcode, X, Y
        (X in 1..n_x, Y in 1..n_y). First column is taken as barcode.
        If you don't have one, see the note below in __main__.
    he_image_path : str
        Full-resolution H&E image with on-image barcode markers.
    corner_X1Y1 / corner_Xmax_Y1 / corner_X1_Ymax : (col, row)
        Pixel positions of three reference spots on the H&E image.
    res_um : float or None
        Physical spot-to-spot distance in microns (used to fill
        scalefactors['microns_pixels']). Optional.
    use_filtered: 使用STARsolo的filtered matrix还是raw matrix, 默认使用filtered;
    use_em: 如果use_filtered=False, 是否使用EM分配的matrix(UniqueAndMult-EM.mtx.gz)还是直接使用raw的matrix.mtx.gz, 默认不使用EM分配的matrix;
    """
    t0 = time.time()
    sub = 'filtered' if use_filtered else 'raw'

    if use_em and not use_filtered:
        mtx_path = os.path.join(starsolo_path, f'Gene/{sub}/UniqueAndMult-EM.mtx.gz')
    else:
        mtx_path = os.path.join(starsolo_path, f'Gene/{sub}/matrix.mtx.gz')
    bc_path = os.path.join(starsolo_path, f'Gene/{sub}/barcodes.tsv')
    feat_path = os.path.join(starsolo_path, f'Gene/{sub}/features.tsv')

    # 1) Read STARsolo matrix (genes x cells -> cells x genes)
    print(f'[{time.strftime("%H:%M:%S")}] Reading {os.path.basename(mtx_path)}')
    X = mmread(mtx_path).astype(int).T.tocsr()
    df_bc = pd.read_csv(bc_path, sep='\t', header=None, names=['barcode'])
    df_ft = pd.read_csv(feat_path, sep='\t', header=None,
                        names=['gene_id', 'gene_symbol', 'feature_type'])

    # 2) Map every barcode to (X, Y)
    print(f'[{time.strftime("%H:%M:%S")}] Mapping barcodes to (X, Y)')

    # 如果你的文件是tab分隔, 用sep='\t'; 默认是有表头的, 用header=0; 如果没有表头, 用header=None并提供names参数;
    bc_pos = pd.read_csv(barcode_position_file, sep='\t', engine='python')
    # 清理列名
    bc_pos.columns = [c.strip() for c in bc_pos.columns]

    # 判断barcode list是否有这几列;
    required_cols = {"barcode", "x", "y"}
    missing_cols = required_cols - set(bc_pos.columns)
    if len(missing_cols) != 0:
        raise ValueError(f"These columns are missing: {missing_cols}")

    # 把小写 x/y 改成后面代码需要的大写 X/Y
    bc_pos = bc_pos.rename(columns={
        'x': 'X',
        'y': 'Y'
    })

    # 只保留 barcode -> X/Y 坐标
    bc_pos = bc_pos.set_index('barcode')[['X', 'Y']].astype(int)

    # 只保留在bc_pos中有坐标的barcode;
    keep = df_bc['barcode'].isin(bc_pos.index).values
    # 同步过滤表达矩阵和barcode表;
    X = X[keep, :]
    df_bc = df_bc.loc[keep].reset_index(drop=True)
    # 按照过滤后的barcode顺序提取坐标;
    coords_raw = bc_pos.loc[df_bc['barcode'].values, ['X', 'Y']].to_numpy()
    
    # 坐标方向变换;
    coords = transform_grid_coords(
        coords_raw, n_x=n_x, n_y=n_y, 
        mode=coord_transform
    )

    # 3) Compute pixel grid for every (X, Y) on the chip
    print(f'[{time.strftime("%H:%M:%S")}] Computing pixel grid')
    pix_full, grid_full, spot_pitch = get_pixel_grid(
        corner_X1Y1, corner_Xmax_Y1, corner_X1_Ymax, n_x=n_x, n_y=n_y)
    # 建立一个字典, 把芯片坐标映射到像素坐标;
    # 比如: (np.int64(60), np.int64(75)): array([13017, 16154])
    grid_to_pix = {tuple(g): p for g, p in zip(grid_full, pix_full)}
    # 根据每个barcode的芯片坐标(X, Y), 查找它在H&E图上的像素坐标;
    pix_obs = np.array([grid_to_pix[(int(x), int(y))] for x, y in coords])

    # 4) H&E image, low/hi-res versions, scalefactors
    print(f'[{time.strftime("%H:%M:%S")}] Loading H&E image')
    he = load_image(he_image_path)
    # 获取H&E原图尺寸;
    h_full, w_full = he.shape[:2]
    # 把H&E图最长边缩放到2000 pixels;
    hires_scalef = round(2000 / max(h_full, w_full), 8)
    # 把H&E图最长边缩放到600 pixels;
    lowres_scalef = round(600 / max(h_full, w_full), 8)
    # 生成hires/lowres图像;
    hires_img  = cv2.resize(he, (int(w_full * hires_scalef),
                                  int(h_full * hires_scalef)))
    lowres_img = cv2.resize(he, (int(w_full * lowres_scalef),
                                  int(h_full * lowres_scalef)))

    # 5) in_tissue from Otsu mask
    if compute_mask:
        mask = get_HE_mask(he)
        # 判断每个spot是否落在tissue上;
        in_tissue = mask[pix_obs[:, 1], pix_obs[:, 0]].astype(int).astype(str)
    else:
        # 如果不计算mask, 就默认所有spot都在组织上;
        in_tissue = np.array(['1'] * len(pix_obs))

    # 6) Assemble AnnData
    obs = pd.DataFrame({
        'sample_id': sample_name,
        'barcode'  : df_bc['barcode'].values,
        'Spot_col' : coords[:, 0],
        'Spot_row' : coords[:, 1],
        'in_tissue': in_tissue,
    }, index=df_bc['barcode'].values)

    var = pd.DataFrame(
        {'gene_symbol': df_ft['gene_symbol'].values,
         'feature_type': df_ft['feature_type'].values},
        index=df_ft['gene_id'].values
    )

    scalefactors = {
        'tissue_hires_scalef' : float(hires_scalef),
        'tissue_lowres_scalef': float(lowres_scalef),
        'spot_diameter_fullres': float(spot_pitch),
    }

    # 如果用户提供了真实物理间距res_um, 就计算微米和像素的关系; 
    if res_um is not None:
        scalefactors['microns_pixels'] = float(res_um / spot_pitch)

    adata = AnnData(
        X=X, obs=obs, var=var,
        obsm={'spatial': pix_obs},
        uns={'spatial': {sample_name: {
            'images': {'hires': hires_img, 'lowres': lowres_img},
            'scalefactors': scalefactors,
        }}}
    )
    # var_names换成symbol_id;
    adata.var['gene_id'] = adata.var_names
    adata.var_names = adata.var['gene_symbol'].astype(str)
    adata.var_names_make_unique()
    adata.var.index.name = None

    # 7) QC
    if species == 'mouse':
        adata.var['mt']   = adata.var['gene_symbol'].str.startswith('mt-')
        adata.var['ribo'] = adata.var['gene_symbol'].str.startswith(('Rps', 'Rpl'))
        adata.var['hb']   = adata.var['gene_symbol'].str.contains(r'^Hb[^p]', regex=True)
    else:  # human / pig / etc default
        adata.var['mt']   = adata.var['gene_symbol'].str.startswith('MT-')
        adata.var['ribo'] = adata.var['gene_symbol'].str.startswith(('RPS', 'RPL'))
        adata.var['hb']   = adata.var['gene_symbol'].str.contains(r'^HB[^P]', regex=True)
    sc.pp.calculate_qc_metrics(adata, qc_vars=['mt', 'ribo', 'hb'], inplace=True)

    in_tis = adata.obs['in_tissue'] == '1'
    print(f"[{time.strftime('%H:%M:%S')}] Done in {time.time()-t0:.1f}s")
    print(f"  total spots         : {adata.n_obs}")
    print(f"  spots in tissue     : {in_tis.sum()}  "
          f"({100*in_tis.mean():.1f}%)")
    if in_tis.sum():
        print(f"  median UMIs (tissue): "
              f"{np.median(adata.obs.total_counts[in_tis]):.0f}")
        print(f"  median genes(tissue): "
              f"{np.median(adata.obs.n_genes_by_counts[in_tis]):.0f}")
    return adata

